const STRINGS: Record<AppLanguage, Record<I18nKey, string>> = {
  en: EN,
  fi: FI,
};

/** Every key in the source-of-truth dictionary, for coverage tests. */
export const I18N_KEYS = Object.keys(EN) as I18nKey[];

export function t(
  language: AppLanguage,
  key: I18nKey,
  vars?: Record<string, string | number>,
): string {
  const template = STRINGS[language]?.[key] ?? EN[key];
  if (!vars) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (match, name: string) =>
    name in vars ? String(vars[name]) : match,
  );
}

export const SUPPORTED_LANGUAGES: Array<{ key: AppLanguage; label: string; flag: string }> = [
  { key: 'fi', label: 'FIN', flag: '🇫🇮' },
  { key: 'en', label: 'ENG', flag: '🇬🇧' },
];

const BODY_PART_KEYS: Record<string, I18nKey> = {
  chest: 'bodyPart.chest',
  back: 'bodyPart.back',
  shoulders: 'bodyPart.shoulders',
  legs: 'bodyPart.legs',
  glutes: 'bodyPart.glutes',
  biceps: 'bodyPart.biceps',
  triceps: 'bodyPart.triceps',
  core: 'bodyPart.core',
  'full body': 'bodyPart.fullBody',
};

/** Display label for a library bodyPart value; unknown values just capitalize. */
export function bodyPartLabel(language: AppLanguage, bodyPart: string): string {
  const key = BODY_PART_KEYS[bodyPart.trim().toLowerCase()];
  if (!key) {
    const trimmed = bodyPart.trim();
    return trimmed.charAt(0).toUpperCase() + trimmed.slice(1);
  }
  return t(language, key);
}
